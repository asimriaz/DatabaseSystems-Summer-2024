-- Script to create Oracle's "SCOTT" schema with tables
-- EMP, DEPT, BONUS, SALGRADE. Originally Oracle's. 
--
CREATE TABLE DEPT
       (DEPTNO INTEGER PRIMARY KEY,
	DNAME VARCHAR(14) ,
	LOC VARCHAR(13) ) ;

CREATE TABLE EMP
       (EMPNO INTEGER PRIMARY KEY,
	ENAME VARCHAR(10),
	JOB VARCHAR(9),
	MGR DECIMAL(4,0),
	HIREDATE DATE,
	SAL DECIMAL(7,2),
	COMM DECIMAL(7,2),
	DEPTNO INTEGER REFERENCES DEPT);

